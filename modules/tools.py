import json

def generate_faq_file(path="data/faq.json"):
    faq = [
        {"q": "как купить премиум", "a": "Премиум покупается через TON в разделе сайта."},
        {"q": "как удалить аккаунт", "a": "У нас можно удалить аккаунт через форму профиля."},
        {"q": "стоит ли доверять боту?", "a": "Да, мы - официальный бот поддержки!"},
        {"q": "когда выйдет обновление?", "a": "Следите за новостями в рассылках!"},
        {"q": "работаете ли в праздники?", "a": "Да, поддержка работает в любой день!"}
    ]
    with open(path, 'w', encoding='utf8') as f:
        json.dump(faq, f, ensure_ascii=False, indent=2)
    print(f"Сгенерирован {path}")
  # Для генерации:
if __name__ == '__main__':
    generate_faq_file()
